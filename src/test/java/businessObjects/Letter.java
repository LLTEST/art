package businessObjects;

public class Letter {

    private final String NAME_1;

    private final String SUBJECT_1;


    public Letter (String name, String subject){
        NAME_1=name;
        SUBJECT_1=subject;
    }


    public String getNAME1() {
        return NAME_1;
    }

    public String getSUBJECT1() {
        return SUBJECT_1;
    }



}